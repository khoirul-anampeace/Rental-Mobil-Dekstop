    private void tampil_tb_mobil(){
    Object []baris = {"Mobil", "Merk", "Tahun", "No Polisi", "No Rangka", "No Mesin", "Warna", "Jumlah Kursi", "Catatan", "Tarif", "Status"};
        tabmode = new DefaultTableModel (null, baris);
        tbl_mobil.setModel(tabmode);
        Connection con = new DatabaseConfig().getConnection();
        try {
            String sql = "SELECT * from mobil order by id_mobil asc";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String id_mobil = hasil.getString("id_mobil");
                String merk = hasil.getString("merk");
                String tahun = hasil.getString("tahun");
                String no_polisi = hasil.getString("no_polisi");
                String no_rangka = hasil.getString("no_rangka");
                String no_mesin = hasil.getString("no_mesin");
                String warna = hasil.getString("warna");
                String jumlah_kursi = hasil.getString("jumlah_kursi");
                String catatan = hasil.getString("catatan");
                String tarif = hasil.getString("tarif");
                String status = hasil.getString("status");
                String[] data = {id_mobil, merk, tahun, no_polisi, no_rangka, no_mesin, warna, jumlah_kursi, catatan, tarif, status};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
            JOptionPane.showMessageDialog(null, "Menampilkan data gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }

