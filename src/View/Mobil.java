    private void id_otomatis(){
        try {
            Connection con = new DatabaseConfig().getConnection();
            Statement stat = con.createStatement();
            ResultSet rs = stat.executeQuery("SELECT * FROM mobil order by id_mobil desc");
            if (rs.next()) {
                 String id = rs.getString("id_mobil").substring(1);
                 String AN = "" + (Integer.parseInt(id) + 1);
                 String nol = "";
                 if (AN.length() == 1) {
                    nol = "000";
                }else if(AN.length() == 2){
                     nol = "00";
                 }else if (AN.length() == 3) {
                     nol = "0";
                }else if (AN.length() == 4) {
                    nol = "";
                }
                 in_id.setText("M" + nol + AN);
            }else{
                in_id.setText("M0001");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
