    public void tampil_tb_sopir(String cari){
    Object []baris = {"Id Sopir","Wilayah Tujuan", "Tarif Sopir"};
        tabmode = new DefaultTableModel (null, baris);
        tbl_sopir.setModel(tabmode);
        Connection con = new DatabaseConfig().getConnection();
        String sql;
        try {
           if(!cari.equals("")){
                sql = "SELECT * FROM sopir WHERE id_sopir LIKE '%"+cari+"%' "
                        + "OR wilayah_tujuan LIKE '%"+cari+"%' "
                        + "OR tarif_sopir LIKE '%"+cari+"%'";
            }else{
                sql = "SELECT * from sopir order by id_sopir asc";
            }
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String id_sopir = hasil.getString("id_sopir");
                String wilayah_tujuan = hasil.getString("wilayah_tujuan");
                String tarif_sopir = hasil.getString("tarif_sopir");
                String[] data = {id_sopir,wilayah_tujuan, tarif_sopir};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
            JOptionPane.showMessageDialog(null, "Menampilkan data gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }

